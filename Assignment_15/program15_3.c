#include<stdio.h>

//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name : CountInRange
//  Description   : It is used to count digits which are in between 3 and 7
//  Input         : int
//  Output        : int
//  Author        : Mukta Dhanraj Mane
//  Date          : 31/10/2025
//
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

int CountInRange(int iNo)
{
    int iDigit = 0;
    int iCnt = 0;

    if(iNo < 0)
    {
        iNo = -iNo;
    }

    while(iNo != 0)
    {
        iDigit = iNo % 10;

        if((iDigit > 3) && (iDigit < 7))
        {
            iCnt++;
        }

        iNo = iNo / 10;
    }

    return iCnt;
}

//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Entry Point function for the application
//
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0;
    int iRet = 0;

    printf("Enter Number : ");
    scanf("%d", &iValue);

    iRet = CountInRange(iValue);

    printf("Count of digits between 3 and 7 is : %d\n", iRet);

    return 0;
}

