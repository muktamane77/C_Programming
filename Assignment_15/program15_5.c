#include<stdio.h>

//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name : DifferenceEvenOdd
//  Description   : It is used to return difference between summation of even digits and summation of odd digits
//  Input         : int
//  Output        : int
//  Author        : Mukta Dhanraj Mane
//  Date          : 31/10/2025
//
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

int DifferenceEvenOdd(int iNo)
{
    int iDigit = 0;
    int iEvenSum = 0;
    int iOddSum = 0;

    if(iNo < 0)
    {
        iNo = -iNo;
    }

    while(iNo != 0)
    {
        iDigit = iNo % 10;

        if(iDigit % 2 == 0)
        {
            iEvenSum = iEvenSum + iDigit;
        }
        else
        {
            iOddSum = iOddSum + iDigit;
        }

        iNo = iNo / 10;
    }

    return (iEvenSum - iOddSum);
}

//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Entry Point function for the application
//
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0;
    int iRet = 0;

    printf("Enter Number : ");
    scanf("%d", &iValue);

    iRet = DifferenceEvenOdd(iValue);

    printf("Difference between summation of even and odd digits is : %d\n", iRet);

    return 0;
}

