#include<stdio.h>

//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name : Percentage
//  Descriptio :    It is used to calculate percentage
//  Input :         Int, Int
//  Output :        float
//  Author :        Mukta Dhanraj Mane
//  Date :          28/10/2025
//
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

float Percentage(int iTotalMarks, int iObtainedMarks)
{
    float result = 0.0;
    if(iTotalMarks == 0)
    {
        return 0.0;
    }

    result = ((float)iObtainedMarks / (float)iTotalMarks) * 100;

    return result;
}

//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Entry Point function for the application
//
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue1 = 0, iValue2 = 0;
    float fRet = 0.0;

    printf("Please enter total marks :");
    scanf("%d",&iValue1);

    printf("PLease enter obtained marks :");
    scanf("%d",&iValue2);

    fRet = Percentage(iValue1, iValue2);
    printf("Percentage is: %.2f%%\n", fRet);

    return 0;
}

