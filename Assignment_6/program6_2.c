bool CheckGreater(int iNo)
{
    if(iNo > 100)
    {
        return true;
    }
    else
    {
        return false;
    }
}

//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Entry Point function for the application
//
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0;
    bool bRet = false;

    printf("Please Enter Number");
    scanf("%d",&iValue);

    bRet = CheckGreater(iValue);

    if(bRet ==  true)
    {
        printf("Greater");

    }
    else
    {
        printf("Smaller");
    }

    return 0;
}

