//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name : CheckGreater
//  Descriptio :    It is used to check number is greater or smaller
//  Input :         Int, Int
//  Output :        bool
//  Author :        Mukta Dhanraj Mane
//  Date :          28/10/2025
