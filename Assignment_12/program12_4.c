//  Function Name : Sum_Of_Even_Factors
//  Descriptio :    It is used to print sum of eve factors
//  Input :         Int, Int
//  Output :        Int
//  Author :        Mukta Dhanraj Mane
//  Date :          30/10/2025
//
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

int Sum_Of_Even_Factors(int iNo)
{
    int iCnt = 0;
    int iSum = 0;

    if(iNo <= 0)
    {
        iNo = -iNo;
    }

    for(iCnt = 2; iCnt <= iNo / 2; iCnt++)
    {
        if((iNo % iCnt == 0) && (iCnt % 2 == 0))
        {
            iSum = iSum + iCnt;
        }
    }
    return iSum;
}

//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Entry Point function for the application
//
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue, iRet;

    printf("Enter Number : ");
    scanf("%d",&iValue);

    iRet = Sum_Of_Even_Factors(iValue);

    printf("Sum of even factors %d is : %d\n", iValue, iRet);

    return 0;
}

