#include<stdio.h>

//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name : count_factors
//  Descriptio :    It is used to print count of factors
//  Input :         Int, Int
//  Output :        Int
//  Author :        Mukta Dhanraj Mane
//  Date :          30/10/2025
//
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

int count_factors(int iNo)
{
    int iCnt = 0;
    int iCount = 0;

    if(iNo <= 0)
    {
        iNo = -iNo;
    }

    for(iCnt = 2; iCnt <= iNo / 2; iCnt++)
    {
        if(iNo % iCnt == 0)
        {
            iCount++;
        }
    }
    return iCount;
}

//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Entry Point function for the application
//
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    int number, iRet;

    printf("Enter Number : ");
    scanf("%d",&number);

    iRet = count_factors(number);

    printf("Number of factors of %d is : %d\n", number, iRet);

    return 0;
}

