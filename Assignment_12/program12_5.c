bool is_divisible_by_five(int iNo)
{
    if(iNo % 5 == 0)
    {
        return true;
    }
    else
    {
        return false;
    }
}

//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Entry Point function for the application
//
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0;
    bool iReb = false;

    printf("Enter Numbe : ");
    scanf("%d",&iValue);

    iReb = is_divisible_by_five(iValue);

    if(iReb == true)
    {
        printf("Number is Divisible by 5");
    }
    else
    {
        printf("Number is NOT Divisible by 5");
    }

    return 0;
}

