void print_factors(int iNo)
{

    int iCnt = 0;

    if(iNo <= 0)
    {
        iNo = -iNo;
    }

    printf("Factors of %d are: ", iNo);

    for(iCnt = 2; iCnt <= iNo / 2; iCnt++)
    {
        if(iNo % iCnt == 0)
        {
            printf("%d\t",iCnt);
        }
    }
}

//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Entry Point function for the application
//
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    int number;

    printf("Enter Number : ");
    scanf("%d",&number);

    print_factors(number);

    return 0;
}

