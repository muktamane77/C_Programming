//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name : DisplayEvenFactors
//  Descriptio :    It is used to print even factors of number
//  Input :         Int, Int
//  Author :        Mukta Dhanraj Mane
//  Date :          27/10/2025
//
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

void DisplayEvenFactors(int iNo)
{
    int iCount = 0;

    if(iNo <= 0)
    {
        iNo = -iNo;
    }

    for(iCount = 1; iCount <= (iNo / 2); iCount++)
    {
        if((iNo % iCount == 0) && (iCount % 2 == 0))
        {
            printf("%d\t",iCount);
        }
    }
}

//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Entry Point function for the application
//
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0;

    printf("Enter Number:\n");
    scanf("%d",&iValue);

    DisplayEvenFactors(iValue);

    return 0;
}

