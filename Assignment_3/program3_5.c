//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name : CheckVowel
//  Descriptio :    It is used to check character is vowel or not
//  Input :         Int, Int
//  Output :        bool
//  Author :        Mukta Dhanraj Mane
//  Date :          28/10/2025
