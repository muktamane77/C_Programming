//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name : DisplayConvert
//  Descriptio :    It is used to convert case of that character
//  Input :         Int, Int
//  Author :        Mukta Dhanraj Mane
//  Date :          27/10/2025
//
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

char DisplayConvert(char cValue)
{
    if (cValue >= 'A' && cValue <= 'Z')
    {
        cValue = cValue + 32;
    }
    else if(cValue >= 'a' && cValue <= 'z')
    {
        cValue = cValue - 32;
    }

    printf("Converted Character: %c\n", cValue);
    return cValue;
}

//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Entry Point function for the application
//
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    char cValue = '\0';

    printf("Enter Character\n");
    scanf("%c", &cValue);

    DisplayConvert(cValue);

    return 0;
}

