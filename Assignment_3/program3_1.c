void PrintEven(int iNo)
{
    int iCnt = 0;
    int iEven = 2;

    if(iNo <= 0)
    {
        iNo = -iNo;
    }

     for(iCnt = 1; iCnt <= iNo; iCnt++)
     {
        printf("%d\t",iEven);
        iEven = iEven + 2;
     }
    
}

