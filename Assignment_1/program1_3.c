void DisplayNumber()
{
    int i = 5;

    while (i >= 1)
    {
        printf("%d\t",i);
        i--;
    }
}

//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Entry Point function for the application
//
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    DisplayNumber();

    return 0;
}

