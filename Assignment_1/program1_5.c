#include<stdio.h>

//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name : Accept
//  Descriptio :    It is used to print *
//  Input :         Int, Int
//  Author :        Mukta Dhanraj Mane
//  Date :          26/10/2025
//
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

void Accept(int iNo){
    int iCnt = 0;
    for(iCnt = 1; iCnt <= iNo; iCnt++)
    {
        printf("*\t");
    }
}

//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Entry Point function for the application
//
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0;

    printf("Enter Number of Stars :");
    scanf("%d",&iValue);
    Accept(iValue);
    return 0;
}

