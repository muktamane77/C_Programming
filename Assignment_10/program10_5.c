#include<stdio.h>

//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name : SquareMeter
//  Descriptio :    It is used to conversion square meter to square feet
//  Input :         Float, Float
//  Output :        double
//  Author :        Mukta Dhanraj Mane
//  Date :          29/10/2025
//
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

double SquareMeter(int iNo)
{
    double dSquareMeter = 0.0;
    dSquareMeter = iNo * 0.0929;

    return dSquareMeter;
}

//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Entry Point function for the application
//
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0;
    double dRet = 0.0;

    printf("Enter area in square feet : ");
    scanf("%d", &iValue);

    dRet = SquareMeter(iValue);

    printf("Conversion is %.2lf Square meter\n", dRet);

    return 0;
}

