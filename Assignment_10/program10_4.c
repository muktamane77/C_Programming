#include<stdio.h>

//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name : FhtoCs
//  Descriptio :    It is used to conversion Fahrenheit to Celsius
//  Input :         Float, Float
//  Output :        double
//  Author :        Mukta Dhanraj Mane
//  Date :          29/10/2025
//
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

double FhtoCs(float fTemp)
{
    double dCelsius = 0.0;

    dCelsius = ((fTemp - 32) * (5.0 / 9.0));

    return dCelsius;
}

//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Entry Point function for the application
//
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    float fValue = 0.0;
    double dRet = 0.0;

    printf("Enter temprature in Fahrenheit : ");
    scanf("%f", &fValue);

    dRet = FhtoCs(fValue);

    printf("Conversion is %.2lf Celsius\n", dRet);

    return 0;
}

