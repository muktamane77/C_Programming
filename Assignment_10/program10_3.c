#include<stdio.h>

//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name : KMtoMeter
//  Descriptio :    It is used to conversion KM to Meter
//  Input :         Float, Float
//  Output :        Float
//  Author :        Mukta Dhanraj Mane
//  Date :          29/10/2025
//
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

int KMtoMeter(int iNo)
{
    int iMeter = 0;

    iMeter = iNo * 1000;

    return iMeter;
}

//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Entry Point function for the application
//
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

int main(){
    int iValue = 0, iRet = 0;

    printf("Enter Distance : ");
    scanf("%d",&iValue);

    iRet = KMtoMeter(iValue);

    printf("Conversion is %d meters\n", iRet);

    return 0;
}

