#include<stdio.h>

//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name : Display
//  Descriptio :    It is used to display first number in second number of times
//  Input :         Int, Int
//  Output:         Int
//  Author :        Mukta Dhanraj Mane
//  Date :          27/10/2025
