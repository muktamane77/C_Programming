void Display(int iNo){
    int iCnt = 0;

    while (iCnt < iNo)
    {
        printf("*\t");
        iCnt ++;
    }
    
}

//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Entry Point function for the application
//
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0;
    printf("Enter Number :\n");
    scanf("%d",&iValue);

    Display(iValue);
    return 0;
}

