bool CheckZero(int iNo)
{
   int iDigit = 0;
   
   if(iNo < 0)
   {
    iNo = - iNo;
   }

   while (iNo != 0)
   {
    iDigit = iNo % 10;

    if(iDigit == 0)
    {
        return true;
    }
    iNo = iNo / 10;
   }
   
}

//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Entry Point function for the application
//
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0;
    bool bRet = false;

    printf("Enter Number : ");
    scanf("%d",&iValue);

    bRet = CheckZero(iValue);

    if(bRet == true)
    {
        printf("It contains Zero");
    }
    else
    {
        printf("There is no Zero");
    }
    return 0;
}

