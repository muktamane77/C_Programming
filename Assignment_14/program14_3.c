//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name : CountTwo
//  Descriptio :    It is used to count frequency of 2
//  Input :         Int, Int
//  Output :        Int
//  Author :        Mukta Dhanraj Mane
//  Date :          30/10/2025
