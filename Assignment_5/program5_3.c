void CheckLeapYear(int year)
{
    if(year % 4 == 0)
    {
        printf("%d is Leap Year\n", year);
    }
    else
    {
        printf("%d is Not Leap Year\n", year);
    }
}

//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Entry Point function for the application
//
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    int yr;
    printf("Enter Year:");
    scanf("%d", &yr);

    CheckLeapYear(yr);

    return 0;
}

