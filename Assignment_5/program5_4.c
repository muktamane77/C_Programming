void CheckNumberType(int num)
{
    if (num > 0)
    {
        printf("%d is Positive\n", num);
    }
    else if(num < 0)
    {
       printf("%d is Negative\n", num); 
    }
    else
    {
        printf("%d Number is Zero\n", num);
    }
}

//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Entry Point function for the application
//
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    int number;

    printf("Enter Number:");
    scanf("%d", &number);

    CheckNumberType(number);

    return 0;
}

