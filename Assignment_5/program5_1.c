void CheckEvenOdd(int iNo)
{
    if(iNo % 2 == 0)
    {
        printf("Number is Even.");
    }
    else
    {
        printf("Number is Odd.");
    }
}

//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Entry Point function for the application
//
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue;

    printf("Enter Number :");
    scanf("%d",&iValue);

    CheckEvenOdd(iValue);

    return 0;
}

