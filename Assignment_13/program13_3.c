//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name : print_odd_numbers
//  Descriptio :    It is used to print ODD numbers
//  Input :         Int, Int
//  Output :        Int
//  Author :        Mukta Dhanraj Mane
//  Date :          30/10/2025
//
