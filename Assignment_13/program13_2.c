//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name : print_even_numbers
//  Descriptio :    It is used to print even numbers
//  Input :         Int, Int
//  Output :        Int
//  Author :        Mukta Dhanraj Mane
//  Date :          30/10/2025
//
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

void print_even_numbers(int iNo)
{
    int iCnt = 0;

    for(iCnt = 1; iCnt <= iNo; iCnt++)
    {
        if(iCnt % 2 == 0)
        {
            printf("%d\t",iCnt);
        }
    }
}

//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Entry Point function for the application
//
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    int limit = 0;

    printf("Enter Number : ");
    scanf("%d",&limit);

    print_even_numbers(limit);

    return 0;
}

