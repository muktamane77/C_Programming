#include<stdio.h>

//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name : SumNatural
//  Descriptio :    It is used to print sum of natural numbers
//  Input :         Int, Int
//  Output :        Int
//  Author :        Mukta Dhanraj Mane
//  Date :          30/10/2025
//
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

int SumNatural(int iNo)
{
    int iCnt = 0, iSum = 0;

    for(iCnt = 1; iCnt <= iNo; iCnt++)
    {
        iSum = iSum + iCnt;
    }

    return iSum;
}

//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Entry Point function for the application
//
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0, iRet = 0;

    printf("Enter Number : ");
    scanf("%d", &iValue);

    iRet = SumNatural(iValue);

    printf("Sum of first %d natural numbers is : %d\n", iValue, iRet);

    return 0;
}

