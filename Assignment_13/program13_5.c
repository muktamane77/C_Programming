//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name : add_even_numbers
//  Descriptio :    It is used to addition of even numbers
//  Input :         Int, Int
//  Output :        Int
//  Author :        Mukta Dhanraj Mane
//  Date :          30/10/2025
