int add_even_numbers(int iNo)
{
    int iCnt = 0;
    int iSum = 0;

    for(iCnt = 1; iCnt <= iNo; iCnt++)
    {
        if(iCnt % 2 == 0)
        {
            iSum = iSum + iCnt;
        }
    }
    return iSum;
}

//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Entry Point function for the application
//
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    int limit = 0;
    int iRet = 0;

    printf("Enter Number : ");
    scanf("%d",&limit);

    iRet = add_even_numbers(limit);

    printf("Addition of even number %d",iRet);

    return 0;
}

