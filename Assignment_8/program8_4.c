//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name : Table
//  Descriptio :    It is used to print table
//  Input :         Int, Int
//  Output :        Int
//  Author :        Mukta Dhanraj Mane
//  Date :          28/10/2025
//
