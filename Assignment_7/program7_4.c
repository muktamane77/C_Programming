//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name : OddDisplay
//  Descriptio :    It is used to print ODD number
//  Input :         Int, Int
//  Output :        Int
//  Author :        Mukta Dhanraj Mane
//  Date :          28/10/2025
//
